            <h3 style="text-align: center; border-bottom: #e80520 2px solid; width: 300px; margin: auto; padding-bottom: 10px; margin-bottom: 38px; margin-top: 100px;">RHYTHM 5 FELLOWSHIP</h3>

            <p>Rhythm 5 fellowship is the parent body that gave birth to many other ministries under the leadership of Pastor Obie Jason Iwuchukwu.</p>

            <ul>
                <li>Healing Center</li>
                <li>Campus Ministry</li>
                <li>Inner City Missions</li>
                <li>The Local Churches</li>
                <li>International Missions</li>
            </ul>

            <h3 style="text-align: center; border-bottom: #e80520 2px solid; width: 300px; margin: auto; padding-bottom: 10px; margin-bottom: 38px; margin-top: 50px;">OUR STATEMENTS</h3>

            <h6> OUR PURPOSE STATEMENT: Why we exist.</h6>
            <p>To lead many people to Christ for the enjoyment of the divine life.</p>

            <h6>OUR VISION STATEMENT: What we intend to do</h6> 
            <p>Raising a generation of overcomers</p>

            <h6> OUR MISSION STATEMENT: what we do </h6>
            <p>We are commissioned to bring the divine life to sinners, train them to grow into the nature of the fullness of Christ and experience the joy of the victorious life in Christ.</p>

            <h6>OUR FAITH STATEMENT: What we believe  </h6>
            <ul>
                <li>We believe and hold the bible as the complete and only divine revelation.</li>
                <li>We strongly believe that God is eternally one and also the Father, Son and Holy Spirit; the three distinct but not    separate.</li>
                <li>We believe that Christ is both the complete God, the perfect man. Without abandoning his divinity, he was conceived in the womb of a human virgin, lived a human life on earth and died a vicarious and all inclusive death on the cross.</li>
                <li>We believe that after 3days, Jesus resurrected bodily and ascended to the third heaven. He is now in the millennium and in eternity.</li>
            </ul>
        
           <h6>Our value statement: What we practice</h6>
           <ul>
               <li>Equipping the believers: through balanced and intensive training</li>
               <li>Intentional and radical evangelism</li>
               <li>Creativity and excellence</li>
               <li>Dynamic worship: we are committed to a worship that is permeated with a strong presence of the Holy Spirit.</li>
           </ul>
                  
            <h3 style="text-align: center; border-bottom: #e80520 2px solid; width: 300px; margin: auto; padding-bottom: 10px; margin-bottom: 38px; margin-top: 50px;">  HISTORY OF THE MINISTRY </h3>
            <p>Rhythm 5 fellowship is a non-denominational, non-racial, and non-tribal ministry which started as a campus fellowship in 2002.The church arm started 2012 by Pastor Obie Jason Iwuchukwu and it is modeled after the bible principle of the church life.</p>


